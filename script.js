function showRight() {
    let elem = document.querySelector(".right-pane")
    elem.style.right = '0'
}

function hideRight() {
    let elem = document.querySelector(".right-pane")
    elem.style.right = '-350px'
}

function viewImage() {
    let elem = document.querySelector(".big-image")
    elem.style.top = "0"
}

function hideImage() {
    let elem = document.querySelector(".big-image")
    elem.style.top = "-100%"
}